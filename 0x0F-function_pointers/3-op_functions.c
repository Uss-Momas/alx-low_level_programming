/**
  * op_add - add's 2 numbers
  * @a: first number
  * @b: second number
  * Return: the sum of a and b
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtracts 2 numbers
  * @a: first number
  * @b: second number
  * Return: the difference of a and b
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiplies 2 numbers
  * @a: first number
  * @b: second number
  * Return: the product of a and b
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - divides 2 numbers
  * @a: first number
  * @b: second number
  * Return: the result of the divion of a and b
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - calculates the reminder of the division of 2 numbers
  * @a: first number
  * @b: second number
  * Return: the reminder of division of a and b
  */
int op_mod(int a, int b)
{
	return (a % b);
}
